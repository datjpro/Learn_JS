//10: toán tử tăng giảm

let a = 99;
let b = 100;
let c = 101;
let d = 102;

// Tăng giá trị của a lên 1 (postfix)
a++;
// Giảm giá trị của b xuống 1 (postfix)
b--;
// Tăng giá trị của c lên 1 (prefix)
++c;
// Giảm giá trị của d xuống 1 (prefix)
--d;
// In ra giá trị của các biến
console.log("Giá trị của a sau khi tăng:", a);
console.log("Giá trị của b sau khi giảm:", b);
console.log("Giá trị của c sau khi tăng:", c);
console.log("Giá trị của d sau khi giảm:", d);
